package one;

/**
 * interface for Shapes
 * 
 * @author Sarah Jeter
 *
 */
public interface Shape {
	/**
	 * find area of the given shape
	 * @return area of shape
	 */
	public double getArea();
	
	/**
	 * find perimeter of the given shape
	 * @return perimeter of shape
	 */
	public double getPerimeter();
}
